document.addEventListener("DOMContentLoaded", () => {
  const params = new URLSearchParams(window.location.search);
  const blogKey = params.get("id");
  const blogData = blogContent.find((elem) => elem.id == blogKey)

  const blogContainer = document.getElementById("blog-blogs-container");

  console.log(blogKey, blogData)

  if (blogContainer) {
    console.log(blogContent)
    blogContent.forEach((blog) => {
      blogContainer.innerHTML += `
        <div class="blog_details_item ul_li">
            <div class="xb-item--img" style="height: 320px; width: 280px; object-fit: cover;">
                <a href="blog-details.html?id=${blog.id}"><img style="height: 100%; width: 100%; object-fit: cover;" src="${blog.thumbnail ?? "assets/img/blog/b-img03.jpg"}" alt="Technology Blog Image"></a>
            </div>
            <div class="xb-item--holder">
                <span class="xb-item--text">Technology</span>
                <h3 class="xb-item--title border-effect">
                    <a href="blog-details.html?id=${blog.id}">${blog.title}</a>
                </h3>
                <p class="xb-item--content">${blog.description.substring(0, 160)}${blog.description.length > 160 ? "..." : ""}</p>
                <div class="xb-item--button mt-50">
                    <a href="blog-details.html?id=${blog.id}">Read more <i class="far fa-arrow-right"></i></a>
                </div>
            </div>
        </div>
      `
    })
    
  } else if (blogKey && blogData) {

    document.getElementById("blog-details-title").textContent = blogData.title;
    document.getElementById("blog-details-subtitle").textContent = blogData.title;
    document.getElementById("blog-details-description").textContent = blogData.description;
    document.getElementById("blog-details-thumbnail").setAttribute("src", blogData.thumbnail);

    const blogContentContainer = document.getElementById("blog-details-content");
    blogContentContainer.innerHTML += blogData.sections.map(
      (elem) => `
        <h3 class="item_details_info_heading">${elem.heading}</h3>
        <p>${elem.content}</p>
      `
    ).join("");

  } else {
    document.querySelector("main").innerHTML = `
      <div class="error-message">
        <h2>Service not found</h2>
        <p>Please select a valid blog from our offerings.</p>
      </div>
    `;
  }
});
